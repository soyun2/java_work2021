
public class pro6_1_1 {
	public static void main(String[] args) {
		cal(9,5);
	}
	
	public static void cal(int a, int b)
	{
		System.out.println("a + b = "+(a+b));
		System.out.println("a - b = "+(a-b));
		System.out.println("a * b = "+(a*b));
		System.out.println("a / b = "+(a/b));
		System.out.println("a % b = "+(a%b));
	}
}
